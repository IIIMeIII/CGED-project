class Role():
    def __init__(self, roleName, baseHP, baseMP, baseStrength, 
                 baseMStrength, baseSpeed, enemyAtks, enemyMAtks):
        self.roleName = roleName
        self.baseHP = baseHP
        self.baseMP = baseMP
        self.baseStrength = baseStrength
        self.baseMStrength = baseMStrength
        self.baseSpeed = baseSpeed
        self.enemyAtks = enemyAtks
        self.enemyMAtks = enemyMAtks